#include "node.hpp"

#include "resourceManager.hpp"

/*-----------------------------------------------------------------------------
*   NAMESPACE: CORE
*-----------------------------------------------------------------------------*/
namespace core
{
/*-----------------------------------------------------------------------------
*   NAMESPACE: NODES
*-----------------------------------------------------------------------------*/
namespace nodes
{
/*-----------------------------------------------------------------------------
*   CONSTRUCTOR
*-----------------------------------------------------------------------------*/
Node::Node()
    :
    m_name("node"),
    m_id(ResourceManager::getInstance().assignNodeID()),
    m_type(nodes::Type::NODE),

    m_initializeFlag(true),
    m_updateFlag(false)
{
}

/*-----------------------------------------------------------------------------
*   DESTRUCTOR
*-----------------------------------------------------------------------------*/
Node::~Node()
{
    ResourceManager::getInstance().removeNodeID(m_id);
}

/*-----------------------------------------------------------------------------
*   SET NAME
*-----------------------------------------------------------------------------*/
void Node::setName(const std::string& name)
{
    m_name = name;
}

/*-----------------------------------------------------------------------------
*   SET INITIALIZE FLAG
*-----------------------------------------------------------------------------*/
void Node::setInitializeFlag(bool value)
{
    m_initializeFlag = value;
}

/*-----------------------------------------------------------------------------
*   SET UPDATE FLAG
*-----------------------------------------------------------------------------*/
void Node::setUpdateFlag(bool value)
{
    m_updateFlag = value;
}

/*-----------------------------------------------------------------------------
*   GET FULL NAME
*-----------------------------------------------------------------------------*/
std::string Node::getFullName() const
{
    return m_name + "#" + std::to_string(m_id);
}

/*-----------------------------------------------------------------------------
*   SET TYPE
*-----------------------------------------------------------------------------*/
void Node::setType(nodes::Type type)
{
    m_type = type;
}
} //NAMESPACE: NODES
} //NAMESPACE: CORE

